def get_detailed_aspect_interpretation(planet1: str, planet2: str, aspect: str, language: str = "it") -> str:
    """
    Ottiene l'interpretazione dettagliata di un aspetto tra due pianeti.
    """
    # Normalizza i nomi dei pianeti
    p1 = planet1.replace("_", " ").title().replace(" ", "_")
    p2 = planet2.replace("_", " ").title().replace(" ", "_")
    
    # Prova entrambe le combinazioni
    key1 = (p1, p2)
    key2 = (p2, p1)
    
    interpretations = DETAILED_ASPECT_INTERPRETATIONS.get(key1) or DETAILED_ASPECT_INTERPRETATIONS.get(key2)
    
    if interpretations:
        aspect_lower = aspect.lower()
        aspect_data = interpretations.get(aspect_lower, {})
        return aspect_data.get(language, aspect_data.get("it", ""))
    
    return ""
